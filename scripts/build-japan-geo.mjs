// 一度だけ実行して src/japan-geo.js を生成するスクリプト。
//   node scripts/build-japan-geo.mjs
// @svg-maps/japan（47都道府県の正確なSVGパス, viewBox 0 0 438 516）を元に、
//  - 都道府県パスをそのまま「日本列島」アウトラインとして束ねる
//  - 8地域へ都道府県をグルーピングし、bboxから地域ノード座標を算出
// ランタイムに地図ライブラリ依存を残さないため、結果を静的JSへ書き出す。

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import japan from '@svg-maps/japan'
import svgpath from 'svgpath'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

// 都道府県 id → 地域
const REGION_OF = {
  hokkaido: 'hokkaido',
  aomori: 'tohoku', iwate: 'tohoku', miyagi: 'tohoku', akita: 'tohoku', yamagata: 'tohoku', fukushima: 'tohoku',
  ibaraki: 'kanto', tochigi: 'kanto', gunma: 'kanto', saitama: 'kanto', chiba: 'kanto', tokyo: 'kanto', kanagawa: 'kanto',
  niigata: 'chubu', toyama: 'chubu', ishikawa: 'chubu', fukui: 'chubu', yamanashi: 'chubu', nagano: 'chubu', gifu: 'chubu', shizuoka: 'chubu', aichi: 'chubu',
  mie: 'kansai', shiga: 'kansai', kyoto: 'kansai', osaka: 'kansai', hyogo: 'kansai', nara: 'kansai', wakayama: 'kansai',
  tottori: 'chugoku', shimane: 'chugoku', okayama: 'chugoku', hiroshima: 'chugoku', yamaguchi: 'chugoku',
  tokushima: 'shikoku', kagawa: 'shikoku', ehime: 'shikoku', kochi: 'shikoku',
  fukuoka: 'kyushu', saga: 'kyushu', nagasaki: 'kyushu', kumamoto: 'kyushu', oita: 'kyushu', miyazaki: 'kyushu', kagoshima: 'kyushu',
  // 沖縄は地図上で左下インセットに描かれるため、地域ノードの重心計算からは除外（landmassとしては描画する）
  okinawa: 'okinawa-inset',
}

// 各 region の代表ノードに使う「主要県」（重心が県全体平均だと海寄りになるため、
// 関東=東京、九州=福岡 のように中心となる県のbbox中心をノード位置に使う）
const ANCHOR_PREF = {
  hokkaido: 'hokkaido',
  tohoku: 'miyagi',
  kanto: 'tokyo',
  chubu: 'aichi',
  kansai: 'osaka',
  chugoku: 'hiroshima',
  shikoku: 'kagawa',
  kyushu: 'fukuoka',
}

function bboxCenter(d) {
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity
  svgpath(d).abs().unarc().unshort().iterate((seg) => {
    // seg = [cmd, ...params]; パラメータは絶対座標のx,yペア（abs後）
    for (let i = 1; i < seg.length - 1; i += 2) {
      const x = seg[i], y = seg[i + 1]
      if (typeof x === 'number' && typeof y === 'number') {
        if (x < minX) minX = x
        if (y < minY) minY = y
        if (x > maxX) maxX = x
        if (y > maxY) maxY = y
      }
    }
  })
  return { cx: (minX + maxX) / 2, cy: (minY + maxY) / 2 }
}

// 都道府県パスを収集
const prefPaths = japan.locations.map((loc) => ({ id: loc.id, d: loc.path }))

// アンカー県のbbox中心から地域ノード座標を算出
const REGION_META = {
  hokkaido: { ja: '北海道', en: 'Hokkaido' },
  tohoku:   { ja: '東北',   en: 'Tohoku' },
  kanto:    { ja: '関東',   en: 'Kanto' },
  chubu:    { ja: '中部',   en: 'Chubu' },
  kansai:   { ja: '関西',   en: 'Kansai' },
  chugoku:  { ja: '中国',   en: 'Chugoku' },
  shikoku:  { ja: '四国',   en: 'Shikoku' },
  kyushu:   { ja: '九州',   en: 'Kyushu' },
}

const regionNodes = {}
for (const [region, prefId] of Object.entries(ANCHOR_PREF)) {
  const loc = japan.locations.find((l) => l.id === prefId)
  const { cx, cy } = bboxCenter(loc.path)
  regionNodes[region] = { x: +cx.toFixed(1), y: +cy.toFixed(1) }
}

// 出力
const header = `// AUTO-GENERATED by scripts/build-japan-geo.mjs — DO NOT EDIT BY HAND.
// 元データ: @svg-maps/japan (47都道府県), viewBox ${japan.viewBox}
`

const body = `export const JAPAN_VIEWBOX = '${japan.viewBox}'

// 47都道府県パス（日本列島アウトラインとして使用）
export const PREFECTURE_PATHS = ${JSON.stringify(prefPaths.map((p) => p.d))}

// 8地域ノード座標（アンカー県のbbox中心）
export const REGION_NODES = ${JSON.stringify(regionNodes, null, 2)}

export const REGION_META = ${JSON.stringify(REGION_META, null, 2)}
`

const out = path.join(__dirname, '..', 'src', 'japan-geo.js')
fs.writeFileSync(out, header + '\n' + body)
console.log('Wrote', out)
console.log('viewBox:', japan.viewBox)
console.log('prefectures:', prefPaths.length)
console.log('region nodes:', JSON.stringify(regionNodes))
